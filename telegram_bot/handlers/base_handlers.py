@base_router.callback_query(F.data == "active_chats")
async def callback_active_chats(callback: CallbackQuery):
    """–ü–æ–∫–∞–∑–∞—Ç—å –∞–∫—Ç–∏–≤–Ω—ã–µ —á–∞—Ç—ã"""
    user = callback.from_user
    telegram_id = int(user.id)
    
    try:
        manager = await manager_service.get_manager_by_telegram_id(telegram_id)
        if not manager:
            await callback.answer("‚ùå –í—ã –Ω–µ –∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞–Ω—ã –∫–∞–∫ –º–µ–Ω–µ–¥–∂–µ—Ä.")
            return
        
        active_chats = await redis_service.get_manager_active_chats(str(telegram_id))
        
        if not active_chats:
            await callback.message.edit_text(
                "üí¨ **–ê–∫—Ç–∏–≤–Ω—ã–µ —á–∞—Ç—ã**\n\n"
                "–£ –≤–∞—Å –Ω–µ—Ç –∞–∫—Ç–∏–≤–Ω—ã—Ö —á–∞—Ç–æ–≤.\n"
                "–ù–æ–≤—ã–µ –∑–∞—è–≤–∫–∏ –±—É–¥—É—Ç –Ω–∞–∑–Ω–∞—á–µ–Ω—ã –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏.",
                reply_markup=InlineKeyboardMarkup(inline_keyboard=[
                    [InlineKeyboardButton(text="‚óÄÔ∏è –ù–∞–∑–∞–¥", callback_data="back_to_main")]
                ])
            )
            return
        
        # –ü–æ–ª—É—á–∞–µ–º –¥–µ—Ç–∞–ª—å–Ω—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ —á–∞—Ç–∞—Ö –∏–∑ –ë–î
        from telegram_bot.models.database import AsyncSessionLocal
        from telegram_bot.models.support_models import SupportChat
        from sqlalchemy import select
        from sqlalchemy.orm import selectinload
        
        chat_details = []
        async with AsyncSessionLocal() as session:
            for chat_id in active_chats[:10]:  # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ø–µ—Ä–≤—ã–µ 10
                result = await session.execute(
                    select(SupportChat)
                    .options(selectinload(SupportChat.manager))
                    .where(SupportChat.chat_id == chat_id, SupportChat.is_active == True)
                )
                chat = result.scalar_one_or_none()
                if chat:
                    chat_details.append(chat)
        
        text = f"üí¨ **–ê–∫—Ç–∏–≤–Ω—ã–µ —á–∞—Ç—ã ({len(chat_details)}/{manager.max_active_chats})**\n\n"
        
        keyboard_buttons = []
        
        if chat_details:
            for i, chat in enumerate(chat_details[:5], 1):  # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ø–µ—Ä–≤—ã–µ 5 —Å –∫–Ω–æ–ø–∫–∞–º–∏
                client_name = chat.client_name or "–ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π"
                phone = chat.client_phone[-4:] if chat.client_phone else "****"
                time_str = chat.last_message_at.strftime('%H:%M') if chat.last_message_at else "‚Äî"
                
                text += f"üî∏ **{i}. {client_name}** (***{phone})\n"
                text += f"   üí¨ {chat.chat_id[:20]}...\n"
                text += f"   üïê –ü–æ—Å–ª–µ–¥–Ω–µ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ: {time_str}\n\n"
                
                # –î–æ–±–∞–≤–ª—è–µ–º –∫–Ω–æ–ø–∫—É –¥–ª—è –∫–∞–∂–¥–æ–≥–æ —á–∞—Ç–∞
                keyboard_buttons.append([
                    InlineKeyboardButton(
                        text=f"üí¨ {i}. {client_name}", 
                        callback_data=f"chat_details_{chat.id}"
                    )
                ])
            
            # –ï—Å–ª–∏ –µ—Å—Ç—å –µ—â–µ —á–∞—Ç—ã, –ø–æ–∫–∞–∑—ã–≤–∞–µ–º –∏—Ö –±–µ–∑ –∫–Ω–æ–ø–æ–∫
            for i, chat in enumerate(chat_details[5:], 6):
                client_name = chat.client_name or "–ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π"
                phone = chat.client_phone[-4:] if chat.client_phone else "****"
                time_str = chat.last_message_at.strftime('%H:%M') if chat.last_message_at else "‚Äî"
                
                text += f"üî∏ **{i}. {client_name}** (***{phone}) - {time_str}\n"
            
            if len(active_chats) > 10:
                text += f"\n... –∏ –µ—â–µ {len(active_chats) - 10} —á–∞—Ç–æ–≤"
        else:
            text += "–£ –≤–∞—Å –Ω–µ—Ç –∞–∫—Ç–∏–≤–Ω—ã—Ö —á–∞—Ç–æ–≤."
        
        # –î–æ–±–∞–≤–ª—è–µ–º –∫–Ω–æ–ø–∫–∏ —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è
        keyboard_buttons.extend([
            [InlineKeyboardButton(text="üîÑ –û–±–Ω–æ–≤–∏—Ç—å", callback_data="active_chats")],
            [InlineKeyboardButton(text="‚óÄÔ∏è –ù–∞–∑–∞–¥", callback_data="back_to_main")]
        ])
        
        keyboard = InlineKeyboardMarkup(inline_keyboard=keyboard_buttons)
        
        await callback.message.edit_text(text, reply_markup=keyboard)
    except Exception as e:
        logger.error(f"‚ùå –û—à–∏–±–∫–∞ –ø–æ–∫–∞–∑–∞ –∞–∫—Ç–∏–≤–Ω—ã—Ö —á–∞—Ç–æ–≤: {e}")
        await callback.answer("‚ùå –ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞.")

